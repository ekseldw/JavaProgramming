package Ch03;

class Jv_3_b1 {
    public static void main(String[] args) {
        int ken = -10, boo;
        boo = -ken;
        System.out.println("* * * * * result * * * * *");
        System.out.println(boo);
        System.out.println(boo++);
        System.out.println(boo);
        System.out.println(--boo);
        System.out.println(boo++);
        System.out.println(boo--);
        System.out.println(++boo);
        System.out.println(boo);
        System.out.println(--boo);
    }    
}