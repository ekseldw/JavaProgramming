package Ch03;

class Jv_3_3 {
    public static void main(String[] args) {
        int x = 50, y = 30;

        System.out.println("* * * * * result * * * * *");
        System.out.println("x < y  -> " + (x < y));
        System.out.println("x <= y -> " + (x <= y));
        System.out.println("x > y  -> " + (x > y));
        System.out.println("x >= y -> " + (x >= y));
        System.out.println("x == y -> " + (x == y));
        System.out.println("x != y -> " + (x != y));
    }
}