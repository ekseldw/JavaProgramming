package Ch03;

class Jv_3_x1 {
    public static void main(String[] args) {
        int x = 100, y = 200;
        
        System.out.println("* * * * * result * * * * *");
        System.out.println("x = " + x + ", y = " + y);
        System.out.println("x++ = " + (x++));
        System.out.println("x   = " + x);
        System.out.println("++x = " + (++x));
        System.out.println("y-- = " + (y--));
        System.out.println("y   = " + y);
        System.out.println("--y = " + (--y));
    }    
}