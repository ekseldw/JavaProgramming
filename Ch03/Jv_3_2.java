class Jv_3_2 {
    public static void main(String[] args ) {
        int x = 100, y = 30;
   
        System.out.println("* * * * * result * * * * *");
        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (x / y));
        System.out.println("x % y = " + (x % y));
        System.out.println("y ^ 3 = " + (y * y * y));
    }
}