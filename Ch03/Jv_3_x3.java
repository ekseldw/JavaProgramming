package Ch03;

class Jv_3_x3 {
    public static void main(String[] args) {
        int x = 123, y = 456;
        boolean a = true, b = false;

        System.out.println("* * * * * result * * * * *");
        System.out.println("x < y   -> " + (x < y));
        System.out.println("x <= y  -> " + (x <= y));
        System.out.println("x > y   -> " + (x > y));
        System.out.println("x >= y  -> " + (x >= y));
        System.out.println("x == y  -> " + (x == y));
        System.out.println("x != y  -> " + (x != y));
        System.out.println("a && b  -> " + (a && b));
        System.out.println("a || b  -> " + (a || b));
        System.out.println("!a      -> " + (!a));
        System.out.println("!b      -> " + (!b));
    }    
}