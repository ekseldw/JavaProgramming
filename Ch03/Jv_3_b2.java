package Ch03;

class Jv_3_b2 {
    public static void main(String[] args) {
        int x = 18, y = 3;

        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (x / y));
        System.out.println("x % y = " + (x % y));
        System.out.println("y ^ 3 = " + (y * y * y));
    }    
}